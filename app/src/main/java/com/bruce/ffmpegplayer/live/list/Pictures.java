package com.bruce.ffmpegplayer.live.list;


public class Pictures {

    private String img;

    public void setImg(String img) {
        this.img = img;
    }

    public String getImg() {
        return img;
    }


    @Override
    public String toString() {
        return "Pictures{" +
                "img='" + img + '\'' +
                '}';
    }
}